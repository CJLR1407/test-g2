
"""Uso del flujo condicional if"""

var1 = int(input("Ingrese su primer valor"))
var2 = int(input("Ingrese su segundo valor"))

if var1 > var2: #Si es verdadero ejecuta su logica interna del flujo condional
    print("El valor de var1 es menor que el segundo")
    print("Es verdadero")
else:
    print("El valor de var1 no es mayor que el segundo")
    print("Es falso")

